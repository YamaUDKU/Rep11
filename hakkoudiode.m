% データの入力
voltage = [0.109, 0.127, 0.15, 0.169, 0.187, 0.211, 0.231, 0.251, 0.28, 0.307, 0.342, 0.414]; % 電圧 (V)
current = [0.001, 0.002, 0.005, 0.010, 0.020, 0.050, 0.100, 0.200, 0.500, 1.000, 2.000, 5.000]; % 電流 (μA)

% プロット
figure; % 新しいウィンドウでプロット
plot(voltage, current, 'o', 'LineWidth', 1.5); % マーカー塗りつぶし
grid on; % グリッドの表示
xlabel('電圧 (V)', 'FontSize', 12); % x軸ラベル
ylabel('電流 (mA)', 'FontSize', 12); % y軸ラベル

% 軸の範囲調整（必要に応じて）
xlim([0.1 0.45]);
ylim([0 6000]);
